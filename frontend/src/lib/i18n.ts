export type T = typeof translations.en;

// ── Hook ─────────────────────────────────────────────────────────────────────

export function useTranslation(): T {
  const { lang } = useContext(LanguageContext);
  const base = translations.en;
  const current = translations[lang];
  if (!current || lang === 'en') return base as unknown as T;
  // Shallow merge per section — missing keys fall back to English
  return {
    common: { ...base.common, ...current.common },
    nav: { ...base.nav, ...current.nav },
    crm: { ...base.crm, ...current.crm },
    hr: { ...base.hr, ...current.hr },
    accounting: { ...base.accounting, ...current.accounting },
    inventory: { ...base.inventory, ...current.inventory },
    workspace: { ...base.workspace, ...current.workspace },
    automation: { ...base.automation, ...current.automation },
    settings: { ...base.settings, ...(current as any).settings },
    orders: { ...base.orders, ...(current as any).orders },
    messages: { ...base.messages, ...(current as any).messages },
    ai: { ...base.ai, ...(current as any).ai },
    login: { ...base.login, ...(current as any).login },
    operations: { ...base.operations, ...(current as any).operations },
    admin: { ...base.admin, ...(current as any).admin },
  } as unknown as T;
}
