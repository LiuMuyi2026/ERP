'use client';

import { useState, useEffect, useContext, createContext, useCallback, createElement } from 'react';
import type { ReactNode } from 'react';

export type LangCode = 'en' | 'zh-CN' | 'zh-TW' | 'ja' | 'it' | 'es' | 'pt';

export const LANGUAGES = [
  { code: 'en',    label: 'English',             native: 'English' },
  { code: 'zh-CN', label: 'Simplified Chinese',  native: '简体中文' },
  { code: 'zh-TW', label: 'Traditional Chinese', native: '繁體中文' },
  { code: 'ja',    label: 'Japanese',            native: '日本語' },
  { code: 'it',    label: 'Italian',             native: 'Italiano' },
  { code: 'es',    label: 'Spanish',             native: 'Español' },
  { code: 'pt',    label: 'Portuguese',          native: 'Português' },
] as const;

const LANG_KEY = 'nexus_ui_lang';

export function getStoredLang(): LangCode {
  if (typeof window === 'undefined') return 'en';
  return (localStorage.getItem(LANG_KEY) as LangCode) || 'en';
}

// ── Language Context ──────────────────────────────────────────────────────────

interface LangContextValue {
  lang: LangCode;
  setLang: (code: LangCode) => void;
}

const LanguageContext = createContext<LangContextValue>({
  lang: 'en',
  setLang: () => {},
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<LangCode>('en');

  useEffect(() => {
    // Read stored preference after mount (avoids SSR mismatch)
    setLangState(getStoredLang());
  }, []);

  const setLang = useCallback((code: LangCode) => {
    localStorage.setItem(LANG_KEY, code);
    setLangState(code);
  }, []);

  return createElement(LanguageContext.Provider, { value: { lang, setLang } }, children);
}

export function useLanguage(): LangContextValue {
  return useContext(LanguageContext);
}

